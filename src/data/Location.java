package data;

public class Location {

	private int absciss;
	private int ordinate;
	
	
	public void setAbsciss(int x) {
		absciss = x;
	}

	public int getAbsciss() {
		return absciss;
	}
	
	
	public void setOrdinate(int y) {
		ordinate = y;
	}

	public int getOrdinate() {
		return ordinate;
	}
}

