package data;

public class Attack {
	
	private double minAttack;
	private double maxAttack;
	
	
	public Attack() {
		
	}
	
	public double getMinAttack() {
		return minAttack;
	}
	public double getMaxAttack() {
		return maxAttack;
	}
	
	public void setMinAttack(double attack) {
		minAttack = attack;
	}
	public void setMaxAttack(double attack) {
		maxAttack = attack;
	}
	
}